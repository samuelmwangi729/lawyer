    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'building'=>'required',
            'box'=>'required',
            'phone'=>'required',
            'email'=>'required',
        ]);
        Contact::create([
            'building'=>$request->building,
            'box'=>$request->box,
            'phone'=>$request->phone,
            'email'=>$request->email
        ]);
        Session::flash('success','The Contacts have been added');
        return redirect('/home')->with('message','Success, Contacts Added');
    }

